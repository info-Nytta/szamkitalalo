<h2>Játék menete</h2>
<p>
	Az egyik játékos gondol egy számra 1-100 között. A másik játékos ezt megpróbálja kitalálni tippeléssel; megjelöl egy számot, az első játékos pedig kiértékeli a "kisebb" / "nagyobb" / "eltaláltad" lehetőségek egyikével.
</p><p>
	<strong>Az a játékos nyer, aki legkésőbb a 7. tippelésre kitalálja a gondolt számot.</strong> Ha nem sikerül a megadott tippelésre eltalálni a gondolt számot, akkor az a játékos kapja a pontot, aki a számra gondolt.
</p>